package org.cloudsmith.geppetto.ui;

import java.text.MessageFormat;

import org.eclipse.emf.common.EMFPlugin;
import org.eclipse.emf.common.ui.EclipseUIPlugin;
import org.eclipse.emf.common.util.ResourceLocator;

/**
 * This is the central singleton for the Geppetto UI plugin.
 */
public final class UIPlugin extends EMFPlugin {

	/**
	 * The actual implementation of the Eclipse <b>Plugin</b>.
	 */
	public static class Implementation extends EclipseUIPlugin {
		/**
		 * Creates an instance.
		 */
		public Implementation() {
			super();

			// Remember the static instance.
			//
			plugin = this;
		}
	}

	/**
	 * Keep track of the singleton.
	 */
	public static final UIPlugin INSTANCE = new UIPlugin();

	/**
	 * Keep track of the singleton.
	 */
	private static Implementation plugin;

	public static String getLocalString(String key, Object... params) {
		String msg = INSTANCE.getString(key);
		return params.length == 0
				? msg
				: MessageFormat.format(msg, params);
	}

	/**
	 * Returns the singleton instance of the Eclipse plugin.
	 * 
	 * @return the singleton instance.
	 */
	public static Implementation getPlugin() {
		return plugin;
	}

	/**
	 * Create the instance.
	 */
	public UIPlugin() {
		super(new ResourceLocator[] {});
	}

	/**
	 * Returns the singleton instance of the Eclipse plugin.
	 * 
	 * @return the singleton instance.
	 */
	@Override
	public ResourceLocator getPluginResourceLocator() {
		return plugin;
	}

}
